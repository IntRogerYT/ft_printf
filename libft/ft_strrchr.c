#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	chr;
	int		i;

	i = ft_strlen(s);
	if (!s && !c)
		return ((char *)&s[i]);
	chr = (char)c;
	while (i >= 0)
	{
		if (s[i] == chr)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
